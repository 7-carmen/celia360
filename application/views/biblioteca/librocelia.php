					Quiero subrayar el amor de Celia por Almería con unos fragmentos, llenos de emoción y lirismo, entresacados, ¡cómo no!, de sus cartas, en este caso a alumnas:Al poco de estar aquí, le escribe a Mª del Mar de la Cámara: “Yo moriría por dar un árbol a mi Almería, lo sabes tú porque lo puse, lo metí en una poesía, lo que no sabes es que estoy realmente muriendo, felizmente muriendo para que en ti, en vosotros, florezcan los pinitos jóvenes”. (Almería, 25 de febrero de 1945).Unos años después, a Mª del Mar Cruz: “Serás tú uno de estos seres magníficos que justifiquen mi vivir de un curso y otro, en mi soledad de Almería, rodeada de libros… ¿Sabes por qué no me voy de Almería? Porque cada año alguien vale la pena. Si algún día me encuentro con el vacío, ¡adiós fandanguillo de Almería, Plaza de la Virgen del Mar! ¡Con las ganas que yo tengo de saber cosicas de Almería cuando me voy!”. (Palma de Mallorca, 24 de agosto de 1947). Ya en los años cincuenta, le expresa a Encarna Romera: “Me gustaría enviarte la hermosura del viento la otra tarde, en la barranca, por los cerros de Almería, o ayer en la Cañada, con el perfume mojado del maíz y la alfalfa… Cuando me dicen que Almería no es hermosa, siempre me indigna. ¡Qué saben! Se piensan que la belleza es la Concha de San Sebastián o los prados para vacas de Santander… Nuevos ricos al fin y al cabo…”. (Almería, 29 de junio de 1953).

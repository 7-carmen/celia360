						Ella escribió, con motivo de tal acto, en un artículo aparecido en el diario Yugo el mismo día de la inauguración y titulado “Biblioteca Francisco Villaespesa”, lo siguiente: “El libro en Almería era difícil… Hoy, 18 de mayo, el estudiante, el obrero, la mujer, el niño, el hombre adánico, en fin, cuando suban el Paseo, el nombre de su poeta familiar los llamará… y la hora del juego, del descanso, del “paseo” podrá remansar en el silencio del libro…”. Destacaron, entre sus múltiples actividades, las conferencias: Dámaso Alonso, Gerardo Diego, García Hoz, Manuel Alvar… Entre las que Celia pronunció, citemos “Arte nuevo y la superación de los mismos”, el 28 de junio de 1952. Igualmente, las exposiciones de pintores locales y nacionales fueron frecuentes: Vázquez Díaz, los indalianos… Celia Viñas escribió algunos catálogos de estas exposiciones: “El agua es pintura en Suárez Egea”, “Arte nuevo de Mallorca”, etc. Numerosos conciertos. También hubo lecturas poéticas, homenajes, etc. La propia Celia recibió un homenaje de la Biblioteca con motivo del accésit obtenido en el Concurso Nacional de Literatura de 1951, el 15 de febrero de 1952. Fruto de la estrecha colaboración entre la Biblioteca y la Universidad de Granada son los Cursos de Extensión Cultural en Almería, ciclos de conferencias impartidos por el profesorado de la universidad granadina. 	

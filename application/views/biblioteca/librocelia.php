					<p class="title">
							
							La influencia de Celia, como pedagoga y sembradora de ilusiones literarias, fue decisiva en las once promociones que pasaron por su cátedra. Como una profesora de nuestros días, les explicó y enseñó a amar a Lorca, Machado, Miguel Hernández… Se alejó de los modelos educacionales vigentes en la posguerra. Celia, junto a nombres como los de Jesús de Perceval, Juan Cuadrado, Hipólito Escolar… tuvo una importancia tan significativa en aquellos años de posguerra que rebasó los límites de Almería. Ella, con su vitalismo y frenética actividad, provocó un despertar cultural en una ciudad, seca y árida en inquietud intelectual, no solo en clase, sino fuera de ella. El impulso y protagonismo por parte de Celia fue decisivo, especialmente en 1947. Dos centros: el Instituto y la Biblioteca Villaespesa y un movimiento -el Indaliano- son los tres grandes focos de la cultura almeriense en ese momento. Nuestra principal biblioteca y Celia constituyen un binomio inseparable. Estuvo en la inauguración del edificio del Paseo con su presencia y con la publicación de un precioso artículo. Hagamos un poco de historia. La Biblioteca Francisco Villaespesa, fundada por iniciativa y bajo el patrocinio del gobernador civil Manuel Urbina Carrera e inaugurada el 18 de mayo de 1947, tuvo como primer director a Hipólito Escolar -después lo fue de la Biblioteca Nacional durante muchos años-, persona muy culta, de talante liberal, técnicamente bien preparado y abierto a todo tipo de iniciativas. Hipólito Escolar supo rodearse de un grupo de colaboradores en los diversos campos de la cultura y del arte. Entre esos nombres, destaca el de Celia Viñas. 
	

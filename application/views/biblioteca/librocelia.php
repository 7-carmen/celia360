					Cursó el Bachillerato, ya en Palma de Mallorca (se trasladaron en 1923), donde recibió la influencia decisiva de su profesor, el catedrático Gabriel Alomar. Se licenció en Filosofía y Letras en la Autónoma de Barcelona en 1941. Allí completó su formación con la asistencia a todas aquellas actividades que destacaron en el panorama cultural catalán. Tras obtener el número uno en las oposiciones a cátedra de Lengua y Literatura de Enseñanza Media, llega a Almería el ocho de marzo de 1943. Enseguida toma cariño a la ciudad, al Instituto, a sus alumnos… 	<br/>
					Afirma en una carta a su familia (Almería, 8 de abril de 1943): “Estoy casi decidida, si no es para reunirme con vosotros, no me muevo de aquí. Además, y no es pisto, desde que yo llegué el Instituto se animó: excursiones, certámenes, conversaciones de arte con los alumnos, incremento del préstamo de libros… Tengo medio embrujadas a las niñas, desconcertados a los niños, embobados a los profesores viejos y algo despistados a los jóvenes que no saben aún realmente cómo han de tomarme”. Fue una profesora entregada a su trabajo, con espíritu moderno, y adelantada a su tiempo.

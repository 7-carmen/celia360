					<p class="title">
						El éxito de la Biblioteca se debió a un grupo de personas con grandes inquietudes culturales, integrado por profesores de los centros docentes de la ciudad (Escuela de Comercio, Escuela de Bellas Artes, Escuela Normal y, especialmente, el Instituto), artistas e intelectuales de la ciudad. En torno a ella nace la asociación “Los Amigos de la Biblioteca Villaespesa”, con más de cuatrocientos miembros -Celia Viñas, carnet número 172- que pagan su cuota, asisten a los actos e intervienen activamente en la selección de libros y en la programación de actividades. Ella, junto al genial artista Jesús de Perceval, es el alma intelectual del Movimiento Indaliano. El 28 de junio de 1947 tuvo lugar en el Museo nacional de Arte Moderno de Madrid la exposición de los pintores indalianos. En el acto de apertura hicieron uso de la palabra José García Nieto y Eugenio d´Ors.  La presentación corrió a cargo de Celia Viñas que, con su verbo cálido y fácil, entusiasmó a los asistentes, especialmente a Eugenio d´Ors quien, a raíz de este acto, escribió una glosa (“Novísimo Glosario: Celia”) de la que entresaco lo siguiente: “La voz sabe a pan, a polvo de carretera, a higo sediento, a uva de piel gorda, caliente de resoles. ¿Y la palabra? Eruptiva y donosa, restellante como un látigo y espiraleante como una caracola, la palabra de Celia salta entre borbotones sin tránsito, como en el desangrarse de una herida…”. La propia Celia comenta a su amiga Pepita Aguiló: “Mi charla sencilla, casi humilde, ha producido emoción verdadera. Se me ha presentado un señor y me ha dado la mano: “Soy Gerardo Diego”. Y detrás, tantos y tantos… Eugenio d´Ors me ha llamado colega y ha dicho que mi tipo de oratoria es completamente nuevo, mío, que soy la novedad. ¡Qué gracia!”.

							Y a Mª Carmen Fernández, dos años antes de su muerte le confiesa (para entender esta cita, debemos recordar que Celia en 1951 consiguió el accésit al Concurso Nacional de Literatura por su obra El primer botón del mundo y trece cuentos más): “La señorita Celia se hace antigua en Almería. Son ya nueve años, niña, hija, son nueve años y son nueve años hermosos que por nada del mundo viviría de otra manera… Ahora que las sirenas han vuelto a cantar a propósito de la reacción de toda España con la noticia de mi pequeño triunfo, una y otra vez aseguro que hice bien en dejar Madrid y venirme a esta tierra seca, buena y tranquila… Si me cupiera otra vez elegir… volvía”. (Almería, 1 de marzo de 1952). Como escritora, cultivó todos los géneros literarios, destacando su faceta lírica, especialmente su poesía infantil. En poesía vieron la luz en vida: Trigo del corazón (1946), Canción tonta en el Sur (1948), Palabras sin voz (1953) y Del foc i la cendra (1953). Y póstumas: Como el ciervo corre herido (1955), Canto (1964), Poesía última (1979). Además, la Antología de Adonais (1976) y Oleaje, publicada por la Voz de Almería, en 2004, con motivo del cincuenta aniversario de su muerte. En prosa, el libro de ensayo, Estampas de la vida de Cervantes, publicado en 1949 por la Biblioteca Villaespesa como homenaje. Obras póstumas en prosa: Plaza de la Virgen del Mar (teatro, coautora Tadea Fuentes, 1974), El primer botón del mundo y trece cuentos más (1980), Viento levante y fragmentos de Tierra del Sur (novelas, IEA, 1991), De esto y aquello (artículos, IEA; 1995) y Cartas de Celia Viñas a su familia (1943-1949) (IEA, 2015).

						¿Qué permanece aún inédito? Algunos poemas sueltos, un cuaderno poético titulado Carmina, una novela, La peregrinita y, especialmente, la mayor parte de sus cartas ya que, ahora en su centenario, solo acaban de salir a la luz las que escribió a su familia desde 1943 a 1949.
